namespace MundorumFabulae.SDL3.Native.MessageBox;

/// <summary>
/// An enumeration of indices inside the colors array of <see cref="MessageBoxColorScheme" />.
/// </summary>
public enum MessageBoxColorType
{
	/// <summary>
	/// The background color.
	/// </summary>
	Background,

	/// <summary>
	/// The text color.
	/// </summary>
	Text,

	/// <summary>
	/// The button border color.
	/// </summary>
	ButtonBorder,

	/// <summary>
	/// The button background color.
	/// </summary>
	ButtonBackground,

	/// <summary>
	/// The button selected color.
	/// </summary>
	ButtonSelected,
}
